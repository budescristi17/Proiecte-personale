SELECT C.ID_COMANDA, C.DATA, C.MODALITATE, A.NUME, A.PRENUME
FROM COMENZI C
LEFT JOIN ANGAJATI A ON C.ID_ANGAJAT = A.ANGAJAT_ID;

--13
SELECT P.NUME_PRODUS, P.PRET_LISTA
FROM PRODUSE P
LEFT JOIN RAND_COMENZI R ON P.PRODUS_ID = R.ID_PRODUS;

--14
SELECT A.NUME, A.SALARIUL, A.DATA_ANGAJARII, D.DENUMIRE_DEPARTAMENT
FROM ANGAJATI A
JOIN DEPARTAMENTE D ON A.ID_DEPARTAMENT = D.ID_DEPARTAMENT
WHERE D.DENUMIRE_DEPARTAMENT LIKE '%Sales%' 
ORDER BY A.SALARIUL ASC, A.DATA_ANGAJARII DESC;

