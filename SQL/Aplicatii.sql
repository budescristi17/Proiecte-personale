ALTER TABLE ANGAJATI 
ADD (ID_DEPARTAMENT NUMBER(4));
UPDATE ANGAJATI SET ID_DEPARTAMENT = 60;
SELECT A.NUME, A.SALARIUL, A.FUNCTIE, D.DENUMIRE_DEPARTAMENT
FROM ANGAJATI A
JOIN DEPARTAMENTE D ON A.ID_DEPARTAMENT = D.ID_DEPARTAMENT
WHERE D.DENUMIRE_DEPARTAMENT = 'IT';

--11
SELECT A.NUME, A.SALARIUL, A.FUNCTIE, D.DENUMIRE_DEPARTAMENT
FROM ANGAJATI A
RIGHT OUTER JOIN DEPARTAMENTE D ON A.ID_DEPARTAMENT = D.ID_DEPARTAMENT
WHERE D.DENUMIRE_DEPARTAMENT LIKE '%IT%';

